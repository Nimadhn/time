def load_transactions(tx_dir: Path) -> list[dict]:
    txs = []
    if not tx_dir.exists():
        return txs
    for path in sorted(tx_dir.glob("tx_*.json")):
        try:
            txs.append(json.loads(path.read_text()))
        except Exception as exc:
            logger.warning("skipping malformed %s: %s", path, exc)
    return txs


def index_by_handle(txs: list[dict]) -> dict[str, list[dict]]:
    """Group transactions by handle (both sent and received)."""
    by_handle: dict[str, list[dict]] = defaultdict(list)
    for tx in txs:
        by_handle[tx["from_handle"]].append(tx)
        if tx["to_handle"] != tx["from_handle"]:
            by_handle[tx["to_handle"]].append(tx)
    # Sort each handle's transactions newest-first.
    for h in by_handle:
        by_handle[h].sort(key=lambda t: t.get("timestamp_iso", ""), reverse=True)
    return by_handle


