def _tx_row(tx: dict) -> str:
    tx_id = tx["id"]
    ts = fmt_iso_human(tx.get("timestamp_iso", ""))
    frm = tx["from_handle"]
    to = tx["to_handle"]
    amount = fmt_short(tx["amount_seconds"])
    blue = tx.get("blue_pct", 100)
    node = tx.get("from_node", "")
    return (
        f"<tr>"
        f"<td class=\"time\">{html.escape(ts)}</td>"
        f"<td class=\"handle\"><a href=\"/handle/{handle_url(frm)}.html\">{html.escape(frm)}</a></td>"
        f"<td class=\"handle\"><a href=\"/handle/{handle_url(to)}.html\">{html.escape(to)}</a></td>"
        f"<td class=\"amount\"><a href=\"/tx/{tx_id}.html\">{html.escape(amount)}</a></td>"
        f"<td><span class=\"blue-pill\">blue {blue}%</span></td>"
        f"<td class=\"handle\" style=\"color:var(--muted)\">{html.escape(node)}</td>"
        f"</tr>"
    )


def build_home(txs: list[dict], handles: Iterable[str]) -> str:
    """Home dashboard with stats block + latest 50 table."""
    total_tx = len(txs)
    total_handles = len(set(handles))

    now = datetime.now(timezone.utc)
    cutoff = now - timedelta(hours=24)

    minted_24h = 0
    largest_24h = 0
    for tx in txs:
        try:
            ts = datetime.strptime(tx["timestamp_iso"], "%Y-%m-%dT%H:%M:%SZ").replace(tzinfo=timezone.utc)
        except Exception:
            continue
        if ts >= cutoff:
            minted_24h += tx["amount_seconds"]
            if tx["amount_seconds"] > largest_24h:
                largest_24h = tx["amount_seconds"]

    # Build the table — newest first, cap at 50.
    txs_sorted = sorted(txs, key=lambda t: t.get("timestamp_iso", ""), reverse=True)
    latest = txs_sorted[:50]

    if latest:
        table_rows = "\n".join(_tx_row(t) for t in latest)
